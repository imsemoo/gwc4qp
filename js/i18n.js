// ============ i18n Engine ============
class I18n {
  constructor() {
    this.currentLang = localStorage.getItem('gwc-lang') || 'ar';
    this.listeners = [];
  }

  t(key) {
    return translations[this.currentLang]?.[key] || translations['en']?.[key] || key;
  }

  setLanguage(lang) {
    if (!translations[lang]) return;
    this.currentLang = lang;
    localStorage.setItem('gwc-lang', lang);

    // Set document direction and language
    const dir = translations[lang].dir;
    document.documentElement.dir = dir;
    document.documentElement.lang = lang;

    // Set font class
    document.body.classList.remove('font-ar', 'font-en');
    document.body.classList.add(translations[lang].fontClass);

    // Update all translatable elements
    this.updateDOM();

    // Notify listeners
    this.listeners.forEach(fn => fn(lang));
  }

  updateDOM() {
    // Update elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = text;
      } else {
        el.textContent = text;
      }
    });

    // Update elements with data-i18n-html attribute (for HTML content)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      el.innerHTML = this.t(key);
    });

    // Update language switcher active state
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('lang-btn--active', btn.dataset.lang === this.currentLang);
    });
  }

  onLanguageChange(fn) {
    this.listeners.push(fn);
  }

  init() {
    this.setLanguage(this.currentLang);

    // Bind language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        this.setLanguage(btn.dataset.lang);
      });
    });
  }
}

// Export singleton
window.i18n = new I18n();
